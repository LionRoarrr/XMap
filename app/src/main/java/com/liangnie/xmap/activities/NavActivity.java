    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_nav);

        Intent intent = getIntent();
        LatLng sPoint = intent.getParcelableExtra("point_start");
        LatLng ePoint = intent.getParcelableExtra("point_end");

        mSPoint = new NaviLatLng(sPoint.latitude, sPoint.longitude);
        mEPoint = new NaviLatLng(ePoint.latitude, ePoint.longitude);

        mSList = new ArrayList<>();
        mEList = new ArrayList<>();

        mSList.add(mSPoint);
        mEList.add(mEPoint);

        mNavView = findViewById(R.id.nav_view);
        mNavView.setAMapNaviViewListener(this);
        mNavView.onCreate(savedInstanceState);

        mNav = AMapNavi.getInstance(this);
        mNav.addAMapNaviListener(this);
    }

